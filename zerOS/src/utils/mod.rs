#[macro_export]
macro_rules! alignment_of {
    (    ) => {  0 };
    (u8  ) => {  1 };
    (i8  ) => {  1 };
    (f8  ) => {  1 };
    (u16 ) => {  2 };
    (i16 ) => {  2 };
    (f16 ) => {  2 };
    (u32 ) => {  4 };
    (i32 ) => {  4 };
    (f32 ) => {  4 };
    (u64 ) => {  8 };
    (i64 ) => {  8 };
    (f64 ) => {  8 };
    (u128) => { 16 };
    (i128) => { 16 };
    (f128) => { 16 };
}